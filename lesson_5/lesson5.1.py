# Генератор случайных числе

import random
print(random.random())
print(random.randrange(0, 1000))
#print(random.randint())
print(random.choice((1, 2, 3, 4, 10, 11))) #  Указываем нашу последоватесьность которую мы уже создали
print(random.sample((1, 2, 3, 4, 10, 11, 111, 23, 44, 56) , 4))
print(random.shuffle((1, 2, 3, 4, 10, 11, 55, 66, 77, 87, 99, 98,11, 44)))  # Разобраться самому

